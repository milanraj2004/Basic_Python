cup = input("Choose your Cup Size --- (Small/ Medium/Large)").lower()

if cup == "small":
    print("Price is 10 Rupees Sir/Ma'am!!!")
elif cup == "medium":
    print("Price is 20 Rupees Sir/Ma'am!!!")
elif cup == "large":
    print("Price is 30 Rupees Sir/Ma'am!!!")
else:
    print("Unknown Cup Size Sir/Ma'am!!!")